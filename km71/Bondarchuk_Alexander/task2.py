"""
Дано список чисел. Порахуйте, скільки в ньому пар елементів, рівних один одному. Вважається, що будь-які два елементи,
рівні один одному утворюють одну пару, яку необхідно порахувати.
"""

text = input() #вводимо дані
text1 = text.split() #розділяємо дані
text1.sort() #сортуємо
text1.reverse() #розвертаємо дані
count = 0 #вводимо лічильник
for i in range(len(text1)-1): #цикл
	if(text[i]==text[i+1]):
		count +=1
print(count)
input()
