"""
Дано список. Виведіть ті його елементи, які зустрічаються в списку тільки один раз. Елементи потрібно виводити в тому
порядку, в якому вони зустрічаються в списку.
"""

list = [int(i) for i in input().split()] #вводимо дані
for i in list:
    n = list.count(i) #визначаю скільки раз елемент зустрічається в списку
    if n == 1:
        print(i, end=' ') #виводимо результат
input()
