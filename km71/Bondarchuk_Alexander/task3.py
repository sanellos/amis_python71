"""
Умова: Дано три цілих числа. Вивести найменше з них.

Вхідні дані: 3 цілих числа.  Кожне число користувач вводить в окремому рядку.

Вихідні дані: вивести ціле число
"""
# вводимо 3 числа
a=int(input())
b=int(input())
c=int(input())
# об'еднуємо їх в список
list=(a, b, c)
# сортуємо їх за збільшенням
check=sorted(list)
# присвоюємо змінній найменше число
result=check[0]
print(result)
